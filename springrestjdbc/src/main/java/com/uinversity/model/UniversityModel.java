package com.uinversity.model;

public class UniversityModel {
	private String name;
	private String address;
	private int noColleges;
	private int noDept;
	private int zipCode;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getAddress() {
		return address;
	}

	public void setAddress(String address) {
		this.address = address;
	}

	public int getNoColleges() {
		return noColleges;
	}

	public void setNoColleges(int noColleges) {
		this.noColleges = noColleges;
	}

	public int getNoDept() {
		return noDept;
	}

	public void setNoDept(int noDept) {
		this.noDept = noDept;
	}

	public int getZipCode() {
		return zipCode;
	}

	public void setZipCode(int zipCode) {
		this.zipCode = zipCode;
	}
}
